/** –ú–æ–∂–Ω–æ –ª–∏ –æ—Ç–ø—Ä–∞–≤–∏—Ç—å —Ñ–æ—Ä–º—É */
const canSubmit = computed(() => {
  // –ü—Ä–æ–≤–µ—Ä—è–µ–º –æ–±—è–∑–∞—Ç–µ–ª—å–Ω—ã–µ –ø–æ–ª—è
  if (!form.title.trim()) return false
  if (form.questions.length === 0) return false
  
  // –ü—Ä–æ–≤–µ—Ä—è–µ–º –∫–∞–∂–¥—ã–π –≤–æ–ø—Ä–æ—Å
  for (const q of form.questions) {
    if (!q.text.trim()) return false
    
    if (q.type === 'single' || q.type === 'multiple') {
      // –î–æ–ª–∂–µ–Ω –±—ã—Ç—å —Ö–æ—Ç—è –±—ã –æ–¥–∏–Ω –ø—Ä–∞–≤–∏–ª—å–Ω—ã–π –æ—Ç–≤–µ—Ç
      const hasCorrect = q.options.some(opt => opt.isCorrect)
      if (!hasCorrect) return false
      
      // –í—Å–µ –≤–∞—Ä–∏–∞–Ω—Ç—ã –¥–æ–ª–∂–Ω—ã –∏–º–µ—Ç—å —Ç–µ–∫—Å—Ç
      const allFilled = q.options.every(opt => opt.text.trim())
      if (!allFilled) return false
    } else if (q.type === 'text') {
      // –î–æ–ª–∂–µ–Ω –±—ã—Ç—å –ø—Ä–∞–≤–∏–ª—å–Ω—ã–π –æ—Ç–≤–µ—Ç
      if (!q.correctAnswer.trim()) return false
    }
  }
  
  return true
})

