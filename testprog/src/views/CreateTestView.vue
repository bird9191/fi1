async function handleSubmit(): Promise<void> {
  if (!canSubmit.value || isSubmitting.value) return
  
  isSubmitting.value = true
  
  try {
    // –§–æ—Ä–º–∏—Ä—É–µ–º –¥–∞–Ω–Ω—ã–µ –¥–ª—è –æ—Ç–ø—Ä–∞–≤–∫–∏
    const testData = {
      type: form.type,
      title: form.title.trim(),
      description: form.description.trim(),
      isPublic: form.isPublic,
      
      // –ù–∞—Å—Ç—Ä–æ–π–∫–∏
      timeLimit: form.timeLimit ?? null,
      passingScore: form.passingScore,
      maxAttempts: form.maxAttempts,
      strictMode: form.strictMode,
      shuffleQuestions: form.shuffleQuestions,
      shuffleOptions: form.shuffleOptions,
      allowTrainingMode: form.allowTrainingMode,
      showHints: form.showHints,
      showExplanations: form.showExplanations,
      
      // –í–æ–ø—Ä–æ—Å—ã
      questions: form.questions.map(q => ({
        text: q.text.trim(),
        type: q.type,
        options: q.type !== 'text' 
          ? q.options.map(opt => ({
              text: opt.text.trim(),
              isCorrect: opt.isCorrect
            }))
          : [],
        correctAnswer: q.type === 'text' ? q.correctAnswer.trim() : '',
        hint: q.hint.trim() || null
      }))
    }
    
    // –°–æ–∑–¥–∞—ë–º —Ç–µ—Å—Ç
    await testsStore.createTest(testData)
    
    // –ü–µ—Ä–µ—Ö–æ–¥–∏–º –Ω–∞ –ø–∞–Ω–µ–ª—å —É–ø—Ä–∞–≤–ª–µ–Ω–∏—è
    router.push('/dashboard')
    
  } catch (error) {
    console.error('–û—à–∏–±–∫–∞ —Å–æ–∑–¥–∞–Ω–∏—è —Ç–µ—Å—Ç–∞:', error)
    alert('–û—à–∏–±–∫–∞ –ø—Ä–∏ —Å–æ–∑–¥–∞–Ω–∏–∏ —Ç–µ—Å—Ç–∞. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ –µ—â—ë —Ä–∞–∑.')
  } finally {
    isSubmitting.value = false
  }
}
